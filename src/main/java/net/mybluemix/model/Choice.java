package net.mybluemix.model;

public class Choice {
	private Number choiceYes;
	private Number choiceNo;
	private String qId;
	
	public Number getChoiceYes() {
		return choiceYes;
	}
	public void setChoiceYes(Number choiceYes) {
		this.choiceYes = choiceYes;
	}
	public Number getChoiceNo() {
		return choiceNo;
	}
	public void setChoiceNo(Number choiceNo) {
		this.choiceNo = choiceNo;
	}
	public String getqId() {
		return qId;
	}
	public void setqId(String qId) {
		this.qId = qId;
	}

}
